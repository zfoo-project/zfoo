package com.zfoo.protocol.model;


/**
 * T cardinal number that is the sum of three and one.
 *
 * @author godotg
 */
public class Quaternion<A, B, C, D> {

    private A a;
    private B b;
    private C c;
    private D d;

    public Quaternion() {
    }

    public Quaternion(A a, B b, C c, D d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public A getA() {
        return a;
    }

    public B getB() {
        return b;
    }

    public C getC() {
        return c;
    }

    public D getD() {
        return d;
    }
}
